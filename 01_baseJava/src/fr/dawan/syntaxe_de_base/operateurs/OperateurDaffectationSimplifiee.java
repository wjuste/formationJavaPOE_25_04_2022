package fr.dawan.syntaxe_de_base.operateurs;

public class OperateurDaffectationSimplifiee {

	public static void main(String[] args) {
		int a = 10;
		a = a + 5;
		
		//Avec les opérateurs d'affectation simplifiée 
		a += 5; //a = a + 5
		a -= 5; //a = a - 5
		a *= 5; //a = a * 5
		a /= 5; //a = a / 5
	}

}

